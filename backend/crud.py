def get_calendar_events(db: Session, user_id: int, start_date: Optional[datetime.datetime] = None, end_date: Optional[datetime.datetime] = None) -> List[models.CalendarEvent]:
    query = db.query(models.CalendarEvent).filter(models.CalendarEvent.user_id == user_id)
    if start_date:
        query = query.filter(models.CalendarEvent.start_time >= start_date)
