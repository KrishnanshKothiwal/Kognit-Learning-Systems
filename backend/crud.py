def get_quiz_history(db: Session, user_id: int, limit: int = 5):
    attempts = db.query(models.UserQuizAttempt)\
        .join(models.Quiz)\
        .filter(models.UserQuizAttempt.user_id == user_id)\
        .order_by(models.UserQuizAttempt.completed_at.desc())\
        .limit(limit)\
        .all()
    
    attempts.reverse() 

    return [
        QuizHistoryItem(
            name=attempt.quiz.title, # Get title from joined quiz
            score=int(round(attempt.score))
        ) for attempt in attempts
    ]

