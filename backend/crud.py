def increment_progress(db: Session, user_id: int, key: str) -> int:
    prog = db.query(models.AchievementProgress).filter(models.AchievementProgress.user_id == user_id, models.AchievementProgress.key == key).first()
    if not prog:
        prog = models.AchievementProgress(user_id=user_id, key=key, count=0)
        db.add(prog)
    prog.count = (prog.count or 0) + 1
    db.commit()
    db.refresh(prog)
    return prog.count

