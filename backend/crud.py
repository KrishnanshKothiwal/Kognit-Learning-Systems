async def get_daily_nudge(db: Session, user_id: int): 
    """Get a personalized nudge based on user's recent journal mood"""
    from services import ai_service
    import json
    
    # Get recent journal entries with mood data
    recent_entries = db.query(models.JournalEntry).filter(
        models.JournalEntry.user_id == user_id
    ).order_by(models.JournalEntry.created_at.desc()).limit(5).all()
    
    # Extract mood information from recent entries
    recent_moods = []
    current_mood = "neutral"
    
    for entry in recent_entries:
        if entry.emotions:
            try:
                emotions_data = json.loads(entry.emotions)
                sentiment = emotions_data.get("sentiment", "neutral")
                recent_moods.append(sentiment)
                if len(recent_moods) == 1:  # Use most recent entry's mood
                    current_mood = sentiment
            except:
                pass
    
    # Use most recent mood if available
    if recent_moods:
        current_mood = recent_moods[0]
    
    # Generate personalized nudge based on mood using AI
    try:
        nudge_content = await ai_service.generate_mood_based_nudge(current_mood, recent_moods)
    except Exception as e:
        print(f"Error generating AI nudge: {e}")
        # Fallback to generic nudges
        nudges = {
            "positive": "Great to see you're feeling positive! Keep up the momentum and tackle your studies with this energy!",
            "negative": "It sounds like you're going through a tough time. Remember to take breaks and be kind to yourself. You've got this!",
            "neutral": "A little progress each day adds up to big results. Keep going!"
        }
        nudge_content = nudges.get(current_mood, "A little progress each day adds up to big results.")
    
    return DailyNudge(content=nudge_content)

