def update_calendar_event(db: Session, event_id: int, user_id: int, **kwargs) -> Optional[models.CalendarEvent]:
    event = get_calendar_event(db, event_id, user_id)
    if not event:
        return None
    for key, value in kwargs.items():
        if hasattr(event, key) and value is not None:
            setattr(event, key, value)
    event.updated_at = datetime.datetime.utcnow()
    db.add(event)
    db.commit()
    db.refresh(event)
    return event

def delete_calendar_event(db: Session, event_id: int, user_id: int) -> bool:
    event = get_calendar_event(db, event_id, user_id)
